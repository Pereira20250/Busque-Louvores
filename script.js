const lista = document.getElementById("lista-louvores");
const cifraView = document.getElementById("cifra-view");
const cifraTexto = document.getElementById("cifra-texto");
const search = document.getElementById("search");
const voltar = document.getElementById("voltar");
const velocidade = document.getElementById("velocidade");
const whatsapp = document.getElementById("whatsapp");

let timer = null;

function renderLouvores(filtro = "") {
  lista.innerHTML = "";
  louvores
    .filter(l => l.nome.toLowerCase().includes(filtro))
    .forEach(louvor => {
      const div = document.createElement("div");
      div.className = "louvor";
      div.textContent = louvor.nome;
      div.onclick = () => abrirCifra(louvor);
      lista.appendChild(div);
    });
}

function abrirCifra(louvor) {
  lista.classList.add("hidden");
  cifraView.classList.remove("hidden");
  cifraTexto.textContent = louvor.cifra;
}

voltar.onclick = () => {
  cifraView.classList.add("hidden");
  lista.classList.remove("hidden");
  pararRolagem();
};

velocidade.onchange = () => {
  pararRolagem();
  const v = parseFloat(velocidade.value);
  if (v > 0) {
    timer = setInterval(() => {
      cifraTexto.scrollTop += v;
    }, 50);
  }
};

function pararRolagem() {
  if (timer) {
    clearInterval(timer);
    timer = null;
  }
}

whatsapp.onclick = () => {
  const telefone = "5599999999999";
  const texto = encodeURIComponent(cifraTexto.textContent);
  window.open(`https://wa.me/${telefone}?text=${texto}`, "_blank");
};

search.oninput = e => renderLouvores(e.target.value.toLowerCase());

renderLouvores();
